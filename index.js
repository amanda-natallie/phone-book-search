// Search for a name in the phone book
const phoneBookSearch = (phoneBook, userInput) => {
  let low = 0;
  let high = phoneBook.length - 1;

  while (low <= high) {
    let mid = Math.floor((low + high) / 2);
    let [midName, midPhone] = phoneBook[mid];
    if (midName === userInput) {
      return midPhone;
    } else if (midName < userInput) {
      low = mid + 1;
    } else {
      high = mid - 1;
    }
  }

  return null;
};

